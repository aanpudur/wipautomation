package com.wip.training.excercise5;

/*
 * Add methods - input two integers
Modulus method - input two integers
Substract
Multiply
Division
Square - input single integer.
 * 
 * */

public class Calculator {
  
  
  
  
  public int add(int num1, int num2){
    return (num1 + num2);
    
    
  }
  
  public int modulas(int num1, int num2){
    return (num1 % num2);
    
  }
  
  public int substract(int num1, int num2){
    return (num1 - num2);
    
    
  }
  
  public double division(int num1, int num2){
    return (num1 / num2);
    
   
    
  }
  
  public int multiply(int num1, int num2){
    return (num1 * num2);
    
   
    
  }
  
  public int square(int num){
    return (num * num);
    
   
    
  }
  

}
